"""v4r(v4재폭등) 회귀 테스트 — 합성 3분봉.

핵심 검증:
  ① 국소 스윙 기준선: 절대 고점 한참 아래의 국소 박스 돌파가 진입으로 잡힘
     (엔켐 2026-07-16 유형). 같은 데이터에서 v2 는 진입 0 이어야 한다.
  ② 룩어헤드 금지: 기준선 하향 갱신 봉에서 같은 봉 진입 불가.
  ③ 갭 손절 계약: 익일 갭다운 시 체결가 = min(시가, 손절선) = 시가.
  ④ 승자 게이트: 손절(0TP) 후 그날 재진입 금지.
  ⑤ 애프터장 신규 진입 손절 -3%.
  ⑥ 오버나이트 미청산 → 범위 끝 EOR 청산.
"""
from datetime import date, datetime, timedelta

from backtest.run_premarket_pullback import (
    evaluate_day_v2, simulate_symbol_v4r,
)
from backtest.toss_client import KST, Bar

D0 = date(2026, 7, 15)   # 전일
D1 = date(2026, 7, 16)   # 진입일
D2 = date(2026, 7, 17)   # 익일

PARAMS = dict(pre_surge=0.05, pullback_min=0.03, support_tol=0.005,
              tp_levels=(0.05, 0.10, 0.15, 0.20, 0.25), stop_pct=0.04,
              consol_bars=3)


def _b(d: date, hh: int, mm: int, o, h, l, c, v=1000) -> Bar:
    return Bar(ts=datetime(d.year, d.month, d.day, hh, mm, tzinfo=KST),
               open=int(o), high=int(h), low=int(l), close=int(c), volume=int(v))


def _prev_day() -> list[Bar]:
    return [_b(D0, 15, 27, 10000, 10000, 10000, 10000)]


def _enkem_day() -> list[Bar]:
    """프리 급등 11000 → 폭락(이탈 연쇄, 기준선 11000→10600→10450 하향)
    → 국소 다지기 → 10480 돌파(절대고점 11000 아래) 진입."""
    return [
        _b(D1, 8, 0, 10800, 11000, 10700, 10900),          # 프리 게이트 (+10%)
        _b(D1, 9, 0, 10800, 10900, 10600, 10700),
        _b(D1, 9, 3, 10700, 10710, 10500, 10520),          # 눌림 등록 base=11000
        _b(D1, 9, 6, 10500, 10520, 10400, 10420),          # 이탈(local 0 → base 유지)
        _b(D1, 9, 9, 10430, 10600, 10450, 10580),          # 반등 local=10600
        _b(D1, 9, 12, 10550, 10560, 10300, 10320),         # 이탈 → base=10600
        _b(D1, 9, 15, 10330, 10450, 10320, 10430),         # 반등 local=10450
        _b(D1, 9, 18, 10400, 10410, 10200, 10240),         # 이탈 → base=10450
        _b(D1, 9, 21, 10240, 10280, 10210, 10260),         # 다지기 1
        _b(D1, 9, 24, 10260, 10300, 10220, 10280),         # 다지기 2
        _b(D1, 9, 27, 10280, 10310, 10230, 10290),         # 다지기 3
        _b(D1, 9, 30, 10300, 10490, 10290, 10480),         # 국소 기준선 10450 돌파 → 진입
    ]


def _run_v4r(bars_by_day: dict[date, list[Bar]], start=D1, end=None, **over):
    ordered = sorted(bars_by_day)
    params = {**PARAMS, **over}
    return simulate_symbol_v4r("TEST", "테스트", bars_by_day, ordered,
                               start, end or ordered[-1], **params)


def test_local_breakout_entry_and_v2_misses_it():
    """① 국소 재돌파: v4r 은 10480 진입, v2(절대고점 11000 고정)는 진입 0."""
    day = _enkem_day() + [
        # TP1(11004) 고가 체결하되 종가는 절대고점(11000) 아래 — 엔켐 유형 유지
        _b(D1, 9, 33, 10500, 11020, 10490, 10950),
        _b(D1, 9, 36, 10700, 10750, 10400, 10420),   # BE(10480) 청산
    ]
    trades = _run_v4r({D0: _prev_day(), D1: day})
    assert len(trades) == 1
    t = trades[0]
    assert t.entry == 10480                      # 절대고점(11000) 재돌파 없이 진입
    assert t.reason == "1TP/BE"
    assert t.day == D1 and t.exit_day == D1
    # 같은 데이터에서 v2 는 진입 없음 (기준선이 절대 고점에 고정)
    assert evaluate_day_v2("TEST", "테스트", day, 10000, **PARAMS) is None


def test_no_same_bar_entry_on_baseline_downgrade():
    """② 룩어헤드: 이탈로 기준선이 내려간 그 봉에서는 종가가 새 기준선 위여도
    진입 불가(다음 봉부터 유효)."""
    day = [
        _b(D1, 8, 0, 10800, 11000, 10700, 10900),
        _b(D1, 9, 0, 10800, 10900, 10600, 10700),
        _b(D1, 9, 3, 10700, 10710, 10500, 10520),     # 눌림 등록
        _b(D1, 9, 6, 10520, 10530, 10460, 10500),     # 다지기 (local=10530)
        _b(D1, 9, 9, 10500, 10540, 10470, 10510),     # 다지기 (local=10540)
        # 지지 이탈(low 10380 < 10500*0.995) + 종가 10535 > 새 기준선(10540)?
        # → 아니: close 10535 < 구 base 11000 → 이탈 분기 → base=10540 하향.
        #   같은 봉 진입은 분기 구조상 불가. 이후 봉도 다지기 미완이라 진입 없음.
        _b(D1, 9, 12, 10450, 10620, 10380, 10535),
        _b(D1, 9, 15, 10560, 10620, 10550, 10600),    # 재돌파지만 hold=0 → 구조 폐기
    ]
    trades = _run_v4r({D0: _prev_day(), D1: day})
    assert trades == []


def test_overnight_gap_down_stop_fills_at_open():
    """③ 갭 손절: 익일 프리장 시가가 손절선 아래면 시가 체결(스탑가 금지)."""
    day1 = _enkem_day() + [
        _b(D1, 9, 33, 10490, 10520, 10460, 10500),    # TP/스탑 미도달 → 보유 지속
        _b(D1, 15, 27, 10490, 10510, 10470, 10500),
    ]
    # 익일 프리장 첫 봉이 9500 갭다운 (스탑 10480*0.96=10060.8 훨씬 아래)
    day2 = [_b(D2, 8, 0, 9500, 9550, 9450, 9520)]
    trades = _run_v4r({D0: _prev_day(), D1: day1, D2: day2}, end=D2)
    assert len(trades) == 1
    t = trades[0]
    assert t.reason == "0TP/SL"
    assert t.exit == 9500                        # min(시가, 스탑) = 시가
    assert t.day == D1 and t.exit_day == D2


def test_loser_gate_blocks_reentry_same_day():
    """④ 승자 게이트: 손절(0TP) 후 같은 날 완벽한 두 번째 셋업도 진입 금지."""
    day = _enkem_day() + [
        _b(D1, 9, 33, 10470, 10480, 10050, 10060),    # 즉시 손절 (0TP/SL)
        # 두 번째 완벽 셋업 (새 사이클: 고점 누적 → 눌림 → 다지기 3봉 → 돌파)
        _b(D1, 9, 36, 10100, 10500, 10090, 10480),
        _b(D1, 9, 39, 10480, 10490, 10150, 10160),    # 눌림 (-3.3%)
        _b(D1, 9, 42, 10160, 10200, 10150, 10180),
        _b(D1, 9, 45, 10180, 10210, 10160, 10190),
        _b(D1, 9, 48, 10190, 10220, 10170, 10200),
        _b(D1, 9, 51, 10250, 10550, 10240, 10540),    # 재돌파 — 그러나 게이트 차단
    ]
    trades = _run_v4r({D0: _prev_day(), D1: day})
    assert len(trades) == 1                      # 손절 1건뿐, 재진입 없음
    assert trades[0].reason == "0TP/SL"


def test_winner_reentry_and_max_entries():
    """승자(≥1TP) 청산 후 재진입 허용 + --max-entries=1 이면 단발."""
    day = _enkem_day() + [
        _b(D1, 9, 33, 10500, 11100, 10490, 11050),    # TP1
        _b(D1, 9, 36, 10600, 10650, 10400, 10420),    # BE 청산 (1TP → 승자)
        # 재진입 사이클 (청산 이후 봉부터 고점 누적)
        _b(D1, 9, 39, 10420, 10800, 10410, 10780),    # 고점 누적 10800
        _b(D1, 9, 42, 10780, 10790, 10450, 10470),    # 눌림 (-3.2%)
        _b(D1, 9, 45, 10470, 10500, 10460, 10480),
        _b(D1, 9, 48, 10480, 10510, 10470, 10490),
        _b(D1, 9, 51, 10490, 10520, 10480, 10500),
        _b(D1, 9, 54, 10550, 10850, 10540, 10820),    # 재돌파 → 2차 진입
        _b(D1, 15, 27, 10800, 10810, 10790, 10800),
    ]
    data = {D0: _prev_day(), D1: day}
    t4 = _run_v4r(data)                          # max_entries=4(기본)
    assert len(t4) == 2 and t4[1].entry == 10820
    t1 = _run_v4r(data, max_entries=1)           # 1이면 재진입 없음
    assert len(t1) == 1


def test_after_hours_entry_uses_tighter_stop():
    """⑤ 애프터장 신규 진입은 손절 -3% (정규장 -4% 아님)."""
    day = [
        _b(D1, 8, 0, 10800, 11000, 10700, 10900),     # 게이트
        _b(D1, 9, 0, 10800, 10900, 10600, 10700),
        _b(D1, 9, 3, 10700, 10710, 10500, 10520),     # 눌림 등록
        # 정규장 내내 다지기 (진입 없음) — 게이트 최소 6봉 가드 충족용 포함
        _b(D1, 9, 6, 10520, 10535, 10508, 10525),
        _b(D1, 9, 9, 10525, 10538, 10510, 10528),
        _b(D1, 9, 12, 10528, 10542, 10512, 10532),
        _b(D1, 15, 27, 10520, 10540, 10505, 10530),
        # 애프터장: 다지기 지속 → 돌파 진입 (base=11000 아님 — hold 만 충족하면
        # 구조 폐기 방지 위해 국소 돌파 시나리오로)
        _b(D1, 15, 33, 10530, 10550, 10510, 10540),
        _b(D1, 15, 36, 10540, 10560, 10520, 10550),
        _b(D1, 15, 39, 10550, 10570, 10530, 10560),
        _b(D1, 16, 0, 10600, 11050, 10590, 11010),    # base 11000 돌파 → 애프터 진입
        # -3%(10679.7) 도달, -4%(10569.6) 미도달 → 애프터 스탑이면 청산돼야
        _b(D1, 16, 3, 10900, 10910, 10650, 10700),
    ]
    trades = _run_v4r({D0: _prev_day(), D1: day})
    assert len(trades) == 1
    t = trades[0]
    assert t.entry == 11010
    assert t.reason == "0TP/SL"
    # 체결 = min(시가 10900, 스탑 11010*0.97=10679.7) → 스탑가
    assert t.exit == int(round(11010 * 0.97))


def test_unclosed_position_exits_eor_at_range_end():
    """⑥ 범위 끝 미청산 → 마지막 유효 봉 종가, 사유 EOR."""
    day1 = _enkem_day() + [
        _b(D1, 9, 33, 10490, 10520, 10460, 10500),
        _b(D1, 15, 27, 10490, 10510, 10470, 10500),
    ]
    day2 = [_b(D2, 9, 0, 10490, 10520, 10460, 10495),
            _b(D2, 15, 27, 10490, 10510, 10470, 10490)]
    trades = _run_v4r({D0: _prev_day(), D1: day1, D2: day2}, end=D2)
    assert len(trades) == 1
    t = trades[0]
    assert t.reason == "0TP/EOR"
    assert t.exit == 10490 and t.exit_day == D2


def test_whipsaw_bar_no_entry_v2_parity():
    """리뷰 F1: 저가 지지 이탈 + 종가 기준선 돌파가 한 봉에 겹치면(휩쏘 장대봉)
    v2 와 동일하게 진입하지 않고 저점 하향 처리."""
    day = [
        _b(D1, 8, 0, 10800, 11000, 10700, 10900),
        _b(D1, 9, 0, 10800, 10900, 10600, 10700),
        _b(D1, 9, 3, 10700, 10710, 10500, 10520),     # 눌림 등록 base=11000
        _b(D1, 9, 6, 10520, 10540, 10505, 10530),     # 다지기 1
        _b(D1, 9, 9, 10530, 10550, 10510, 10540),     # 다지기 2
        _b(D1, 9, 12, 10540, 10560, 10520, 10550),    # 다지기 3 (hold=3)
        # 휩쏘: 저가 10100(이탈) + 종가 11050(기준선 위) — 진입 금지
        _b(D1, 9, 15, 10500, 11100, 10100, 11050),
        _b(D1, 15, 27, 11000, 11010, 10990, 11000),
    ]
    assert _run_v4r({D0: _prev_day(), D1: day}) == []
    assert evaluate_day_v2("TEST", "테스트", day, 10000, **PARAMS) is None


def test_gap_up_open_fills_tps_before_stop():
    """리뷰 F2: 익일 시가가 TP1·TP2 위 + 저가가 스탑 아래인 갭업 역전 봉 —
    시간순대로 TP 2개는 시가 체결, 잔량만 스탑(BE) 처리."""
    day1 = _enkem_day() + [
        _b(D1, 9, 33, 10490, 10520, 10460, 10500),    # 보유 지속
        _b(D1, 15, 27, 10490, 10510, 10470, 10500),
    ]
    # 진입 10480: TP1=11004, TP2=11528. 익일 시가 11600(둘 다 위), 저가 9000
    day2 = [_b(D2, 9, 0, 11600, 11650, 9000, 9100)]
    trades = _run_v4r({D0: _prev_day(), D1: day1, D2: day2}, end=D2)
    assert len(trades) == 1
    t = trades[0]
    assert t.reason == "2TP/BE"                  # 시가 TP 2개 + 잔량 BE(=진입가)
    expected = (11600 * 2 + 10480 * 3) / 5       # 시가체결 2 + BE 3
    assert t.exit == int(round(expected))


def test_v2_equivalent_single_shot():
    """단순 v2 패턴(이탈 없음)에서 v4r 첫 트레이드가 v2 와 동일 진입/청산."""
    day = [
        _b(D1, 8, 0, 10800, 11000, 10700, 10900),     # 게이트, pre_high=11000
        _b(D1, 9, 0, 10800, 10900, 10600, 10700),
        _b(D1, 9, 3, 10700, 10710, 10500, 10520),     # 눌림 등록 base=11000
        _b(D1, 9, 6, 10520, 10540, 10505, 10530),     # 다지기 1
        _b(D1, 9, 9, 10530, 10550, 10510, 10540),     # 다지기 2
        _b(D1, 9, 12, 10540, 10560, 10520, 10550),    # 다지기 3
        _b(D1, 9, 15, 10600, 11100, 10590, 11050),    # 11000 돌파 → 진입 11050
        _b(D1, 9, 18, 11100, 11700, 11090, 11650),    # TP1(11602.5) 체결
        _b(D1, 9, 21, 11300, 11350, 11000, 11040),    # BE(11050) 청산
        _b(D1, 15, 27, 11000, 11010, 10990, 11000),
    ]
    v2 = evaluate_day_v2("TEST", "테스트", day, 10000, **PARAMS)
    v4r = _run_v4r({D0: _prev_day(), D1: day})
    assert v2 is not None and len(v4r) == 1
    assert v4r[0].entry == v2.entry
    assert v4r[0].reason == v2.reason == "1TP/BE"
    assert v4r[0].exit == v2.exit
